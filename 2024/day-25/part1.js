function keyFitsLock(key, lock) {
    // console.log(key)
    // console.log(lock)
    let fits = true
    key.forEach((pin, index) => {
        if (lock[index] > pin) {
            fits = false
        }
    })
    // console.log(fits)
    return fits
}

function parseData(data) {
    const keys = []
    const locks = []

    for (let i = 0; i < (data.length + 1) / 8; i++) {
        if (/#####/.test(data[i * 8])) {
            locks.push(shapeToPins(false, data.slice(i * 8, i * 8 + 7)))
        } else {
            keys.push(shapeToPins(true, data.slice(i * 8, i * 8 + 7)))
        }
    }

    return { keys, locks }
}

function shapeToPins(isKey, shape) {
    const pins = [0, 0, 0, 0, 0]
    shape.forEach(row => {
        row.split("").forEach((value, index) => {
            if (isKey) {
                value === "." && (pins[index] += 1)
            }
            if (!isKey) {
                value === "#" && (pins[index] += 1)
            }
        })
    })
    return pins
}